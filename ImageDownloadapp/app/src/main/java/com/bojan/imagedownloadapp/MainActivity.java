    ImageView downloadedImage;

    public void downloadImage(View view){

        //  https://upload.wikimedia.org/wikipedia/en/9/9e/Cloud_Strife.png
        ImageDownloader task = new ImageDownloader();
        Bitmap myImage;

        try {

            myImage = task.execute("https://upload.wikimedia.org/wikipedia/en/9/9e/Cloud_Strife.png").get();
            downloadedImage.setImageBitmap(myImage);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

